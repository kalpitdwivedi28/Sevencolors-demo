
$(document).ready(function(){
  $(window).scroll(function(){
  	var scroll = $(window).scrollTop();
	  if (scroll > 600) {
	    $(".welcomeimages_01").fadeOut(5000);
	  }

	  else{
		  $(".welcomeimages_01").fadeIn(3000);  	
	  }
  })
})

